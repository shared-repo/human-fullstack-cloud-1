
// 1. class는 자바 프로그램의 기본 단위
//    모든 자바 프로그램의 구성 요소는 class 내부에 포함됩니다.
public class HelloJava {
	
	// 2. static void main() : 프로그램의 시작 지점
	public static void main(String[] args) {
		
		// 3. 주석
		// 주석1 : 여기서 부터 끝까지 주석
		// 주석2 : 여기서 부터 끝까지 주석
		/* 주석 영역 */
		/* 주석 영역 1
		 주석 영역 2
		 주석 영역3 */
		
		// 6. "..." : 문자열 (문자집합을 표시하는 방법)
		System.out.println("Hello, Java Programming World"); // 4. 모든 명령문의 끝에는 ; 표시
		System.out.println("System.out.println");
		System.out.println("내가 만든 첫 번째 프로그램입니다.");
		System.out.println("끝!!!");
		
		// 5.
		// system.out.println("대소문자 테스트"); // 오류 : 자바는 대소문자 구분

	}

}
